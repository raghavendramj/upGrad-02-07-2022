function switchStatementExample(x) {
  switch (x) {
    case 1:
      console.log("The Color is GREEN");
      break;
    case 2:
      console.log("The Color is RED");
      break;
    case 3:
      console.log("The Color is BLACK");
      break;
    default:
      console.log("INVALID Color");
      break;
  }
}

switchStatementExample(7);
